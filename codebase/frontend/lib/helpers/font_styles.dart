import 'package:flutter/material.dart';

TextStyle regular(double? size) => TextStyle(
      fontWeight: FontWeight.w400,
      fontSize: size,
    );
TextStyle bold(double? size) => TextStyle(
      fontWeight: FontWeight.w700,
      fontSize: size,
    );
TextStyle medium(double? size) => TextStyle(
      fontWeight: FontWeight.w500,
      fontSize: size,
    );
TextStyle semiBold(double? size) => TextStyle(
      fontWeight: FontWeight.w600,
      fontSize: size,
    );
TextStyle light(double? size) => TextStyle(
      fontWeight: FontWeight.w300,
      fontSize: size,
    );
